package org.example.practice.lesson3;

public class SwitchPractise {
    public static void main(String[] args) {
        int n = 150;
        switch (n){
            case 5:
                System.out.println( "Hello");
                break;
            case 7:
                System.out.println( "Ciao");
                break;
            default:
                System.out.println("Good Night");
        }
    }
}
